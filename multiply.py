a = float(input("Insert a number here : "))
m = input("Do u add, subtract, multiply or divide? : ")
b = float(input("Insert a second number here : "))
if m == 'add':
    print(a + b)
if m == 'subtract':
    print(a - b)
if m == 'multiply':
    print(a * b)
if m == 'divide':
    print(a / b)


















